const mongoose = require("mongoose");

// User Schema

const UserSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true } // this creates the createdAt and updatedAt values
);

module.exports = mongoose.model("User", UserSchema);
